#[derive(Debug, Clone)]
pub struct BuiltinFunction {
    pub name: &'static str,
    pub signature: &'static str,
    pub description: &'static str,
    pub params: &'static [ParamInfo],
    pub return_type: &'static str,
    pub category: Category,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Category {
    Math,
    String,
    List,
    Entity,
    SelectionSet,
    Conversion,
    FileIO,
    Display,
    Geometry,
    ControlFlow,
    ErrorHandling,
    TypeChecking,
    UserInput,
    Symbol,
    Dialog,
    ActiveX,
    Reactor,
    Application,
    Memory,
    Misc,
}

#[derive(Debug, Clone)]
pub struct ParamInfo {
    pub name: &'static str,
    pub description: &'static str,
}

pub fn lookup(name: &str) -> Option<&'static BuiltinFunction> {
    let upper = name.to_uppercase();
    BUILTINS.iter().find(|b| b.name == upper)
}

pub fn all() -> &'static [BuiltinFunction] {
    &BUILTINS
}

pub fn by_prefix(prefix: &str) -> Vec<&'static BuiltinFunction> {
    let upper = prefix.to_uppercase();
    BUILTINS
        .iter()
        .filter(|b| b.name.starts_with(&upper))
        .collect()
}

macro_rules! params {
    () => { &[] };
    ( $( $name:expr => $desc:expr ),+ $(,)? ) => {
        &[ $( ParamInfo { name: $name, description: $desc } ),+ ]
    };
}

